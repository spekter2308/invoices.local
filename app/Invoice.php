<?php

namespace App;

use Carbon\Carbon;
use function foo\func;
use Illuminate\Database\Eloquent\Model;
use App\Helper\HasManyRelation;

class Invoice extends Model
{
    use HasManyRelation;

    protected $guarded = [];

    protected $with = ['customer', 'company', 'settings', 'notes'];

    protected static function boot()
    {
        parent::boot(); // TODO: Change the autogenerated stub

        static::deleting(function($invoice) {
            $invoice->settings->delete();
        });

    }

    public function notes()
    {
        return $this->hasMany(InvoiceAdditionalNotes::class);
    }

    public function items()
    {
        return $this->hasMany(Item::class);
    }

    public function customer()
    {
        return $this->belongsTo(Customer::class);
    }

    public function company()
    {
        return $this->belongsTo(Company::class);
    }

    public function histories()
    {
        return $this->hasMany(InvoiceHistory::class);
    }

    public function payments()
    {
        return $this->hasMany(PaymentInvoice::class);
    }

    public function sentMails()
    {
        return $this->hasMany(InvoiceMail::class);
    }

    public function settings()
    {
        return $this->hasOne(InvoiceSettings::class);
    }

    public function createItems($items)
    {
        foreach ($items as $item) {
            $this->items()->create($item);
        }

        return $this;
    }

    public function scopeFilter($query, $filters)
    {
        return $filters->apply($query);
    }

    public function getPdfInvoiceDateAttribute()
    {
        $date = Carbon::parse($this->attributes['invoice_date']);

        if ($this->settings->date_format == 'dd.MM.yyyy') {
            $format = 'd.m.Y';
        } elseif ($this->settings->date_format == 'dd/MM/yyyy') {
            $format = 'd/m/Y';
        } elseif ($this->settings->date_format == 'MM/dd/yyyy') {
            $format = 'm/d/Y';
        } elseif ($this->settings->date_format == 'dd-MM-yyyy') {
            $format = 'd-m-Y';
        } else {
            $format = 'Y-m-d';
        }

        return $date->format($format);
    }

    public function getPdfDueDateAttribute()
    {
        $date = Carbon::parse($this->attributes['due_date']);

        if ($this->settings->date_format == 'dd.MM.yyyy') {
            $format = 'd.m.Y';
        } elseif ($this->settings->date_format == 'dd/MM/yyyy') {
            $format = 'd/m/Y';
        } elseif ($this->settings->date_format == 'MM/dd/yyyy') {
            $format = 'm/d/Y';
        } elseif ($this->settings->date_format == 'dd-MM-yyyy') {
            $format = 'd-m-Y';
        } else {
            $format = 'Y-m-d';
        }

        return $date->format($format);
    }

    public function setAmountPaidAttribute($value)
    {
        $this->attributes['amount_paid'] = round($value, 2);
    }

    public function setSubtotalAttribute($value)
    {
        $this->attributes['subtotal'] = round($value, 2);
    }

    public function setTotalAttribute($value)
    {
        $this->attributes['total'] = round($value, 2);
    }

    public function setBalanceAttribute($value)
    {
        $this->attributes['balance'] = round($value, 2);
    }
}
